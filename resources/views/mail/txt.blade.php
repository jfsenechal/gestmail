Bonjour,

La ville de Marche procède à la modernisation de son système de messagerie afin d’améliorer la sécurité et les fonctionnalités.

Interruption prévue :
Le jeudi 22 janvier, de 10h00 à 14h00, le serveur mail sera temporairement inaccessible.

Impact :
Pendant cette période, vous ne pourrez ni envoyer ni recevoir d’e-mails, ni accéder à votre boîte mail via webmail ou client (Outlook, Thunderbird etc.). Les e-mails envoyés vers votre adresse seront mis en file d’attente et délivrés automatiquement une fois le service rétabli.

Cette mise à jour est principalement technique et ne devrait pas affecter votre utilisation quotidienne.
Toutefois, des ajustements pourraient être nécessaires au cours de la journée et dans les jours suivants.

Nous vous remercions pour votre compréhension.

L'équipe informatique de la Ville de Marche-en-Famenne.
